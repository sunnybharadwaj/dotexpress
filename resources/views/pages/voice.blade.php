                                                                    <div class="max-w-3xl">

                                                                        <p>My new Indian role model is an amazing old
                                                                            woman. She is strong, independent, goal
                                                                            oriented and incredibly hardworking and has
                                                                            stuck by her tiring schedule every day for
                                                                            more than thirty years now. The villagers
                                                                            and daily wage laborers in and around her
                                                                            tiny village have known her and admired her
                                                                            work all these years. Its only in September
                                                                            this year, that India Today and businessman
                                                                            Anand Mahindra made her a popular hero.</p>

                                                                        <p>I am speaking about K. Kamalathai, the
                                                                            celebrity eighty year old ‘pattima’ or
                                                                            grandmother from Vadivelapayalam village in
                                                                            Tamil Nadu. This amazing woman has been
                                                                            preparing more than one thousand idlis
                                                                            everyday and selling them with freshly
                                                                            prepared sambar and chutney for more than
                                                                            three decades.</p>

                                                                        <p>She started doing this for two reasons. One,
                                                                            because her family of farmers and farm
                                                                            workers would leave her at home while they
                                                                            went to work on the fields all day. Two,
                                                                            because she saw daily wage workers spending
                                                                            a big portion of their wages on their
                                                                            morning breakfast. She wanted people who
                                                                            worked hard to eat healthy food that's not
                                                                            expensive. Also, she didn’t like being bored
                                                                            all alone at home! So she started her
                                                                            business to provide healthy food and to keep
                                                                            herself busy! Most of us would have watched
                                                                            TV, read books or listened to music instead.
                                                                            But not this lady!</p>

                                                                        <p>What makes her so inspiring to me is, she is
                                                                            not a rich woman who has got people working
                                                                            with her or a fancy kitchen with electronic
                                                                            machines. She doesn’t even have a gas stove
                                                                            or a grinder or a refrigerator. This simple
                                                                            lady doesn’t have a team of assistants to
                                                                            help her. She lives in a mostly kuccha house
                                                                            and uses a firewood stove and hand held
                                                                            stone grinder. She has only one idli making
                                                                            vessel that makes 37 idlis at one go. She
                                                                            starts her day before sunrise, takes a bath,
                                                                            completes her prayers and then collects
                                                                            vegetables from the farm. She prepares the
                                                                            sambar and chutney on her own. The chutney
                                                                            is hand ground. The idli batter is ground
                                                                            and left for fermentation the previous
                                                                            night. She uses fresh batter every morning
                                                                            as she has no refrigerator to store it. From
                                                                            6 am till 12 pm, she opens the doors of her
                                                                            home and personally serves every customer a
                                                                            healthy breakfast for an unbelievable amount
                                                                            of one rupee an idli. She serves people and
                                                                            keeps cooking at the same time! Her goal is
                                                                            to only feed people. She says it brings her
                                                                            joy and keeps her active.</p>

                                                                        <p>People have asked her to increase the price,
                                                                            but she refuses because she doesn’t want to
                                                                            make a profit, but only earn enough to keep
                                                                            cooking and serving food the next day. She
                                                                            inspires me. Now I think very carefully
                                                                            about what I need and what I want before I
                                                                            spend my pocket money. Because I know now
                                                                            that I can do good things with every rupee I
                                                                            have. Even the Re.1 coin.</p>

